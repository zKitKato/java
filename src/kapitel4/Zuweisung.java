package kapitel4;

public class Zuweisung {
    public static void main(String[] args) {
        int i;

        int m = 10;

        i = 4;
        System.out.print("Nach der ersten Zuweisung beträgt der Wert der Variablen ");
        System.out.println(i);
        i = m;
        System.out.print("Nach der zweiten Zuweisung beträgt der Wert der Variablen ");
        System.out.println(i);
        i = 8*2;
        System.out.print("Nach der dritten Zuweisung beträgt der Wert der Variablen ");
        System.out.println(i);
    }
}
