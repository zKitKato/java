package kapitel4;

public class VariableTyp {
    public static void main(String[] args) {
        int i;

        double x, y;

        i = 5/2;

        x = 5/2;

        y = 5/2.0;

        System.out.print("Der Wert dieser Variable i beträgt ");
        System.out.println(i);

        System.out.print("Der Wert dieser Variable x beträgt ");
        System.out.println(x);

        System.out.print("Der Wert dieser Variable y beträgt ");
        System.out.println(y);
    }
}
