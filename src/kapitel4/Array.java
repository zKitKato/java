package kapitel4;

public class Array {
    public static void main(String[] args) {
        int[] a;

        a = new int[5];


        a[0] = 3;

        a[1] = 15;

        a[2] = 7;

        a[3] = 12;

        a[4] = 6;


        System.out.print("Der Wert des ersten Feldes beträgt ");
        System.out.println(a[0]);

        System.out.print("Der Wert des zweiten Feldes beträgt ");
        System.out.println(a[1]);

        System.out.print("Der Wert des dritten Feldes beträgt ");
        System.out.println(a[2]);

        System.out.print("Der Wert des vierten Feldes beträgt ");
        System.out.println(a[3]);

        System.out.print("Der Wert des fünften Feldes beträgt ");
        System.out.println(a[4]);
    }
}
